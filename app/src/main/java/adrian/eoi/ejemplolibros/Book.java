package adrian.eoi.ejemplolibros;

public class Book {

    String titulo;
    String Autor;
    String tapaUrl;

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getAutor() {
        return Autor;
    }

    public void setAutor(String autor) {
        Autor = autor;
    }

    public String getTapaUrl() {
        return tapaUrl;
    }

    public void setTapaUrl(String tapaUrl) {
        this.tapaUrl = tapaUrl;
    }
}
